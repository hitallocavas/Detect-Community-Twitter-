package grafo;

public class Frequence {
	private int freq;
	private String term;
	
	public Frequence() {
		
	}

	public int getFreq() {
		return freq;
	}

	public String getTerm() {
		return term;
	}

	public void setFreq(int freq) {
		this.freq = freq;
	}

	public void setTerm(String term) {
		this.term = term;
	}

}
